package JavaException1;

public class StudentResult 
{
	int noOfSubFail;
	int marks;
	int totalScore;
	public int getNoOfSubFail() {
		return noOfSubFail;
	}
	public void setNoOfSubFail(int noOfSubFail) {
		this.noOfSubFail = noOfSubFail;
	}
	public int getMarks() {
		return marks;
	}
	public void setMarks(int marks) {
		this.marks = marks;
	}
	public int getTotalScore() {
		return totalScore;
	}
	public void setTotalScore(int totalScore) {
		this.totalScore = totalScore;
	}
	
}
